/*
 * ox03c10 hdr operating mode
 *
 * Note: Please confirm if the SoC supports RAW16
 *
 * Each Pixel has two photo diodes with independent
 *   exposure gain controls to extend the dynamic range.
 *
 * LPD: Large Photo Diode
 *   LPD for HCG, LCG and VS
 * SPD: Small Photo Diode
 *
 * DCG: Dual Conversion Gain
 *   HCG: High Conversion Gain
 *   LCG: Low Conversion Gain
 * VS: Very Short Exposure
 *
 * PWL: PieceWise Linear
 *   HDR3: PWL12, PWL14, PWL16
 *   HDR4: PWL12, PWL14, PWL16, PWL20
 *
 * LFM: LED Flicker Mitigation
 *
 * HDR3: DCG (HCG + LCG) + VS or DCG (HCG + LCG) + SPD
 *   uncompressed 20bit
 * HDR4: DCG (HCG + LCG) + SPD + VS
 *   uncompressed 24bit
 */
enum ox03c10_hdr_operating_mode {
	OX03C10_HDR3_DCG_VS_12BIT = 0,
	OX03C10_HDR3_DCG_SPD_12BIT = 1,
	OX03C10_HDR3_DCG_VS_LFM_16BIT = 2,
	OX03C10_HDR_OPERATING_MODE_MAX,
};

