static int ox03c10_check_sensor_id(struct ox03c10 *ox03c10)
{
	struct i2c_client *client = ox03c10->client;
	struct device *dev = &client->dev;
	u32 sensor_id = 0;
	int ret = 0, loop = 0;

	for (loop = 0; loop < 3; loop++) {
		if (loop != 0) {
			dev_info(dev, "check sensor id retry (%d)", loop);
			msleep(10);
		}

		ret = ox03c10_i2c_read_reg(client, OX03C10_REG_CHIP_ID,
					OX03C10_REG_VALUE_16BIT, &sensor_id);
		if (ret == 0) {
			if (sensor_id != OX03C10_CHIP_ID) {
				dev_err(dev, "Unexpected sensor id(%06x)\n", sensor_id);
				return -ENODEV;
			} else {
				dev_info(dev, "Detected OV%06x sensor\n", OX03C10_CHIP_ID);
				return 0;
			}
		}
	}

	dev_err(dev, "Check sensor id error, ret = %d\n", ret);

	return -ENODEV;
}

/* Note: In the Serdes scheme, the function must run after start stream */
static int ox03c10_get_dcg_and_spd_ratio(struct ox03c10 *ox03c10)
{
	struct device *dev = &ox03c10->client->dev;
	u32 val = 0;
	int ret = 0;

	ox03c10->dcg_ratio.integer = 0;
	ret |= ox03c10_i2c_read_reg(ox03c10->client, 0x7057,
				OX03C10_REG_VALUE_24BIT, &val);
	ox03c10->dcg_ratio.decimal = val & 0x1ffff;
	ret |= ox03c10_i2c_read_reg(ox03c10->client, 0x705b,
				OX03C10_REG_VALUE_24BIT, &val);
	ox03c10->dcg_ratio.div_coeff = val & 0x1ffff;
	if (ret != 0 || val == 0)
		dev_err(dev, "get dcg ratio fail, ret %d, dcg ratio %d, %d\n",
			ret, ox03c10->dcg_ratio.integer, ox03c10->dcg_ratio.decimal);
	else
		dev_info(dev, "get dcg ratio reg val integer %d, dec 0x%x, div 0x%x\n",
			 ox03c10->dcg_ratio.integer, ox03c10->dcg_ratio.decimal, ox03c10->dcg_ratio.div_coeff);

	ox03c10->spd_ratio.integer = 0;
	ox03c10->spd_ratio.decimal = val & 0x1ffff;
	ret |= ox03c10_i2c_read_reg(ox03c10->client, 0x705f,
				OX03C10_REG_VALUE_24BIT, &val);
	ox03c10->spd_ratio.div_coeff = val & 0x1ffff;
	if (ret != 0 || val == 0)
		dev_err(dev, "get spd ratio fail, ret %d, spd ratio %d, %d\n",
			ret, ox03c10->spd_ratio.integer, ox03c10->spd_ratio.decimal);
	else
		dev_info(dev, "get spd ratio reg val integer %d, dec 0x%x div 0x%x\n",
			 ox03c10->spd_ratio.integer, ox03c10->spd_ratio.decimal, ox03c10->spd_ratio.div_coeff);

	return ret;
}

static int __ox03c10_start_stream(struct ox03c10 *ox03c10)
{
	maxim_remote_ser_t *remote_ser = ox03c10->remote_ser;
	struct i2c_client *client = ox03c10->client;
	struct device *dev = &client->dev;
	int ret = 0;

	if (remote_ser == NULL) {
		dev_err(dev, "%s: remote_ser error\n", __func__);
		return -EINVAL;
	}

	if (remote_ser->ser_ops == NULL) {
		dev_err(dev, "%s: remote_ser ser_ops error\n", __func__);
		return -EINVAL;
	}

	ret = remote_ser->ser_ops->ser_module_init(remote_ser);
	if (ret) {
		dev_err(dev, "%s: remote_ser module_init error\n", __func__);
		return ret;
	}

	ret = ox03c10_check_sensor_id(ox03c10);
	if (ret) {
		dev_err(dev, "%s: ox03c10 check sensor id error\n", __func__);
		return ret;
	}

	ret = ox03c10_i2c_write_array(client, ox03c10->cur_mode->reg_list);
	if (ret) {
		dev_err(dev, "%s: ox03c10 reg_list write array error\n", __func__);
		return ret;
	}

	if (ox03c10->cur_mode->hdr_mode == NO_HDR) {
		ret = ox03c10_i2c_write_array(client, ox03c10->cur_mode->linear_reg_list);
		if (ret) {
			dev_err(dev, "%s: ox03c10 linear_reg_list write array error\n", __func__);
			return ret;
		}
	}

	/* In case these controls are set before streaming */
	ret = __v4l2_ctrl_handler_setup(&ox03c10->ctrl_handler);
	if (ret)
		return ret;
	if (ox03c10->has_init_exp && ox03c10->cur_mode->hdr_mode != NO_HDR) {
		ret = ox03c10_ioctl(&ox03c10->subdev,
				    PREISP_CMD_SET_HDRAE_EXP,
				    &ox03c10->init_hdrae_exp);
		if (ret) {
			dev_err(&ox03c10->client->dev,
				"init exp fail in hdr mode\n");
			return ret;
		}
	}

	if (ox03c10->has_init_wbgain) {
		ret = ox03c10_ioctl(&ox03c10->subdev,
				   RKMODULE_SET_WB_GAIN,
				   &ox03c10->init_wbgain);
		if (ret) {
			dev_err(&ox03c10->client->dev,
				"init wbgain fail\n");
			return ret;
		}
	}

	/* streaming control register */
	ret = ox03c10_i2c_write_reg(client,
				OX03C10_REG_CTRL_MODE,
				OX03C10_REG_VALUE_08BIT,
				OX03C10_MODE_STREAMING);
	if (ret) {
		dev_err(dev, "%s: ox03c10 start stream error\n", __func__);
		return ret;
	}

	ret = remote_ser->ser_ops->ser_pclk_detect(remote_ser);
	if (ret) {
		dev_err(dev, "%s: remote_ser pclk_detect error\n", __func__);
		return ret;
	}

	/* note: get dcg and spd ratio after start stream */
	ret = ox03c10_get_dcg_and_spd_ratio(ox03c10);
	if (ret)
		dev_warn(dev, "get dcg and spd ratio failed\n");

	return 0;
}

static int __ox03c10_stop_stream(struct ox03c10 *ox03c10)
{
	maxim_remote_ser_t *remote_ser = ox03c10->remote_ser;
	struct i2c_client *client = ox03c10->client;
	struct device *dev = &client->dev;
	int ret = 0;

	ox03c10->has_init_exp = false;
	ox03c10->has_init_wbgain = false;

	/* streaming control register */
	ret = ox03c10_i2c_write_reg(client,
				OX03C10_REG_CTRL_MODE,
				OX03C10_REG_VALUE_08BIT,
				OX03C10_MODE_SW_STANDBY);
	if (ret) {
		dev_err(dev, "%s: ox03c10 stop stream error\n", __func__);
		return ret;
	}

	if (remote_ser == NULL) {
		dev_err(dev, "%s: remote_ser error\n", __func__);
		return -EINVAL;
	}

	if (remote_ser->ser_ops == NULL) {
		dev_err(dev, "%s: remote_ser ser_ops error\n", __func__);
		return -EINVAL;
	}

	ret = remote_ser->ser_ops->ser_module_deinit(remote_ser);
	if (ret) {
		dev_err(dev, "%s: remote_ser module_deinit error\n", __func__);
		return ret;
	}

	return 0;
}

static int ox03c10_s_stream(struct v4l2_subdev *sd, int on)
{
	struct ox03c10 *ox03c10 = v4l2_get_subdevdata(sd);
	struct i2c_client *client = ox03c10->client;
	int ret = 0;

	dev_info(&client->dev, "%s: on = %d\n", __func__, on);

	mutex_lock(&ox03c10->mutex);
	on = !!on;
	if (on == ox03c10->streaming)
		goto unlock_and_return;

	if (on) {
		ret = pm_runtime_get_sync(&client->dev);
		if (ret < 0) {
			pm_runtime_put_noidle(&client->dev);
			goto unlock_and_return;
		}

		ret = __ox03c10_start_stream(ox03c10);
		if (ret) {
			v4l2_err(sd, "start stream failed while write regs\n");
			pm_runtime_put(&client->dev);
			goto unlock_and_return;
		}
	} else {
		__ox03c10_stop_stream(ox03c10);
		pm_runtime_put(&client->dev);
	}

	ox03c10->streaming = on;

unlock_and_return:
	mutex_unlock(&ox03c10->mutex);

	return ret;
}

