static int ox03c10_set_hdrae(struct ox03c10 *ox03c10,
			    struct preisp_hdrae_exp_s *ae)
{
	int ret = 0;
	u32 l_dgain = 1024;
	u32 m_dgain = 1024;
	u32 s_dgain = 1024;
	u32 l_exp = ae->long_exp_reg;
	u32 m_exp = ae->middle_exp_reg;
	u32 s_exp = ae->short_exp_reg;
	u32 l_again = ae->long_gain_reg;
	u32 m_again = ae->middle_gain_reg;
	u32 s_again = ae->short_gain_reg;

	if (!ox03c10->has_init_exp && !ox03c10->streaming) {
		ox03c10->init_hdrae_exp = *ae;
		ox03c10->has_init_exp = true;
		dev_dbg(&ox03c10->client->dev, "ox03c10 don't stream, record exp for hdr!\n");
		return ret;
	}

	dev_dbg(&ox03c10->client->dev,
		"rev exp req: L_exp: 0x%x, 0x%x, M_exp: 0x%x, 0x%x S_exp: 0x%x, 0x%x\n",
		l_exp, l_again, m_exp, m_again, s_exp, s_again);

	if (l_exp < 4)
		l_exp = 4;
	if (s_exp < 1)
		s_exp = 1;
	if (s_exp > 35 && ox03c10->cur_mode->exp_mode == EXP_HDR3_DCG_VS)
		s_exp = 35;

	if (l_again < 16) {
		l_again = 16;
	} else if (l_again <= 31) {
	} else if (l_again <= 47) {
		l_again = (l_again - 16) << 1;
	} else if (l_again <= 63) {
		l_again = (l_again - 32) << 2;
	} else if (l_again <= 95) {
		l_again = (l_again - 48) << 3;
	} else if (l_again >= 248) {
		l_dgain = div_u64(l_again * 1024, 248);
		l_again = 248;
	} else {
		dev_err(&ox03c10->client->dev, "%s set l_gain val:0x%x not support",
			__func__, l_again);
		return -EINVAL;
	}
	if (m_again < 16) {
		m_again = 16;
	} else if (m_again <= 31) {
	} else if (m_again <= 47) {
		m_again = (m_again - 16) << 1;
	} else if (m_again <= 63) {
		m_again = (m_again - 32) << 2;
	} else if (m_again <= 95) {
		m_again = (m_again - 48) << 3;
	} else if (m_again >= 248) {
		m_dgain = div_u64(m_again * 1024, 248);
		m_again = 248;
	} else {
		dev_err(&ox03c10->client->dev, "%s set m_gain val:0x%x not support",
			__func__, m_again);
		return -EINVAL;
	}
	if (s_again < 16) {
		s_again = 16;
	} else if (s_again <= 31) {
	} else if (s_again <= 47) {
		s_again = (s_again - 16) << 1;
	} else if (s_again <= 63) {
		s_again = (s_again - 32) << 2;
	} else if (s_again <= 95) {
		s_again = (s_again - 48) << 3;
	} else if (s_again >= 248) {
		s_dgain = div_u64(s_again * 1024, 248);
		s_again = 248;
	} else {
		dev_err(&ox03c10->client->dev, "%s set s_gain val:0x%x not support",
			__func__, s_again);
		return -EINVAL;
	}

	dev_dbg(&ox03c10->client->dev,
		"l_again 0x%x l_dgain 0x%x, m_again 0x%x m_dgain 0x%x, s_again 0x%x s_dgain 0x%x\n",
		l_again, l_dgain, m_again, m_dgain, s_again, s_dgain);
	ret |= ox03c10_write_reg(ox03c10->client, OX03C10_GROUP_UPDATE_ADDRESS,
				 OX03C10_REG_VALUE_08BIT,
				 OX03C10_GROUP_UPDATE_START_DATA);
	// dcg exposure
	ret |= ox03c10_write_reg(ox03c10->client, OX03C10_REG_EXPOSURE_DCG_H,
				 OX03C10_REG_VALUE_16BIT,
				 l_exp);
	// hcg real gain
	ret |= ox03c10_write_reg(ox03c10->client,
				 OX03C10_REG_AGAIN_HCG_H,
				 OX03C10_REG_VALUE_08BIT,
				 (l_again >> 4) & 0x0f);
	ret |= ox03c10_write_reg(ox03c10->client,
				 OX03C10_REG_AGAIN_HCG_L,
				 OX03C10_REG_VALUE_08BIT,
				 (l_again << 4) & 0xf0);
	// hcg digital gain
	ret |= ox03c10_write_reg(ox03c10->client,
				 OX03C10_REG_DGAIN_HCG_H,
				 OX03C10_REG_VALUE_08BIT,
				 (l_dgain >> 10) & 0xf);
	ret |= ox03c10_write_reg(ox03c10->client,
				 OX03C10_REG_DGAIN_HCG_M,
				 OX03C10_REG_VALUE_08BIT,
				 (l_dgain >> 2) & 0xff);
	ret |= ox03c10_write_reg(ox03c10->client,
				 OX03C10_REG_DGAIN_HCG_L,
				 OX03C10_REG_VALUE_08BIT,
				 (l_dgain << 6) & 0xc0);
	// lcg real gain
	ret |= ox03c10_write_reg(ox03c10->client,
				 OX03C10_REG_AGAIN_LCG_H,
				 OX03C10_REG_VALUE_08BIT,
				 (m_again >> 4) & 0x0f);
	ret |= ox03c10_write_reg(ox03c10->client,
				 OX03C10_REG_AGAIN_LCG_L,
				 OX03C10_REG_VALUE_08BIT,
				 (m_again << 4) & 0xf0);
	// lcg digital gain
	ret |= ox03c10_write_reg(ox03c10->client,
				 OX03C10_REG_DGAIN_LCG_H,
				 OX03C10_REG_VALUE_08BIT,
				 (m_dgain >> 10) & 0xf);
	ret |= ox03c10_write_reg(ox03c10->client,
				 OX03C10_REG_DGAIN_LCG_M,
				 OX03C10_REG_VALUE_08BIT,
				 (m_dgain >> 2) & 0xff);
	ret |= ox03c10_write_reg(ox03c10->client,
				 OX03C10_REG_DGAIN_LCG_L,
				 OX03C10_REG_VALUE_08BIT,
				 (m_dgain << 6) & 0xc0);

	if (ox03c10->cur_mode->exp_mode == EXP_HDR3_DCG_VS) {
		// vs exposure
		ret |= ox03c10_write_reg(ox03c10->client, OX03C10_REG_EXPOSURE_VS_H,
					 OX03C10_REG_VALUE_16BIT,
					 s_exp);
		// vs real gain
		ret |= ox03c10_write_reg(ox03c10->client,
					 OX03C10_REG_AGAIN_VS_H,
					 OX03C10_REG_VALUE_08BIT,
					 (s_again >> 4) & 0x0f);
		ret |= ox03c10_write_reg(ox03c10->client,
					 OX03C10_REG_AGAIN_VS_L,
					 OX03C10_REG_VALUE_08BIT,
					 (s_again << 4) & 0xf0);
		// vs digital gain
		ret |= ox03c10_write_reg(ox03c10->client,
					 OX03C10_REG_DGAIN_VS_H,
					 OX03C10_REG_VALUE_08BIT,
					 (s_dgain >> 10) & 0xf);
		ret |= ox03c10_write_reg(ox03c10->client,
					 OX03C10_REG_DGAIN_VS_M,
					 OX03C10_REG_VALUE_08BIT,
					 (s_dgain >> 2) & 0xff);
		ret |= ox03c10_write_reg(ox03c10->client,
					 OX03C10_REG_DGAIN_VS_L,
					 OX03C10_REG_VALUE_08BIT,
					 (s_dgain << 6) & 0xc0);
	} else if (ox03c10->cur_mode->exp_mode == EXP_HDR3_DCG_SPD) {
		// spd exposure
		ret |= ox03c10_write_reg(ox03c10->client, OX03C10_REG_EXPOSURE_SPD_H,
					 OX03C10_REG_VALUE_16BIT,
					 s_exp);
		// spd real gain
		ret |= ox03c10_write_reg(ox03c10->client,
					 OX03C10_REG_AGAIN_SPD_H,
					 OX03C10_REG_VALUE_08BIT,
					 (s_again >> 4) & 0x0f);
		ret |= ox03c10_write_reg(ox03c10->client,
					 OX03C10_REG_AGAIN_SPD_L,
					 OX03C10_REG_VALUE_08BIT,
					 (s_again << 4) & 0xf0);
		// spd digital gain
		ret |= ox03c10_write_reg(ox03c10->client,
					 OX03C10_REG_DGAIN_SPD_H,
					 OX03C10_REG_VALUE_08BIT,
					 (s_dgain >> 10) & 0xf);
		ret |= ox03c10_write_reg(ox03c10->client,
					 OX03C10_REG_DGAIN_SPD_M,
					 OX03C10_REG_VALUE_08BIT,
					 (s_dgain >> 2) & 0xff);
		ret |= ox03c10_write_reg(ox03c10->client,
					 OX03C10_REG_DGAIN_SPD_L,
					 OX03C10_REG_VALUE_08BIT,
					 (s_dgain << 6) & 0xc0);
	}

	ret |= ox03c10_write_reg(ox03c10->client, OX03C10_GROUP_UPDATE_ADDRESS,
				 OX03C10_REG_VALUE_08BIT,
				 OX03C10_GROUP_UPDATE_END_DATA);
	ret |= ox03c10_write_reg(ox03c10->client, OX03C10_GROUP_UPDATE_ADDRESS,
				 OX03C10_REG_VALUE_08BIT,
				 OX03C10_GROUP_UPDATE_LAUNCH);
	return ret;
}

static int ox03c10_get_dcg_and_spd_ratio(struct ox03c10 *ox03c10)
{
	struct device *dev = &ox03c10->client->dev;
	u32 val = 0;
	int ret = 0;

	ret = ox03c10_write_reg(ox03c10->client, OX03C10_REG_CTRL_MODE,
			  OX03C10_REG_VALUE_08BIT, OX03C10_MODE_STREAMING);
	usleep_range(3000, 5000);
	ox03c10->dcg_ratio.integer = 0;
	ret |= ox03c10_read_reg(ox03c10->client, 0x7057,
				OX03C10_REG_VALUE_24BIT, &val);
	ox03c10->dcg_ratio.decimal = val & 0x1ffff;
	ret |= ox03c10_read_reg(ox03c10->client, 0x705b,
				OX03C10_REG_VALUE_24BIT, &val);
	ox03c10->dcg_ratio.div_coeff = val & 0x1ffff;
	if (ret != 0 || val == 0)
		dev_err(dev, "get dcg ratio fail, ret %d, dcg ratio %d, %d\n",
			ret, ox03c10->dcg_ratio.integer, ox03c10->dcg_ratio.decimal);
	else
		dev_info(dev, "get dcg ratio reg val integer %d, dec 0x%x, div 0x%x\n",
			 ox03c10->dcg_ratio.integer, ox03c10->dcg_ratio.decimal, ox03c10->dcg_ratio.div_coeff);

	ox03c10->spd_ratio.integer = 0;
	ox03c10->spd_ratio.decimal = val & 0x1ffff;
	ret |= ox03c10_read_reg(ox03c10->client, 0x705f,
				OX03C10_REG_VALUE_24BIT, &val);
	ox03c10->spd_ratio.div_coeff = val & 0x1ffff;
	if (ret != 0 || val == 0)
		dev_err(dev, "get spd ratio fail, ret %d, spd ratio %d, %d\n",
			ret, ox03c10->spd_ratio.integer, ox03c10->spd_ratio.decimal);
	else
		dev_info(dev, "get spd ratio reg val integer %d, dec 0x%x div 0x%x\n",
			 ox03c10->spd_ratio.integer, ox03c10->spd_ratio.decimal, ox03c10->spd_ratio.div_coeff);

	ox03c10_write_reg(ox03c10->client, OX03C10_REG_CTRL_MODE,
			  OX03C10_REG_VALUE_08BIT, OX03C10_MODE_SW_STANDBY);
	return ret;
}

