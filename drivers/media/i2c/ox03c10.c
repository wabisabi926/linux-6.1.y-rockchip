static int ox03c10_select_exp_mode(struct ox03c10 *ox03c10, u32 exp_mode)
{
	int ret = -EINVAL;
	u32 i, h, w, hdr_mode;

	w = ox03c10->cur_mode->width;
	h = ox03c10->cur_mode->height;
	hdr_mode = ox03c10->cur_mode->hdr_mode;
	for (i = 0; i < ARRAY_SIZE(supported_modes); i++) {
		if (w == supported_modes[i].width &&
		    h == supported_modes[i].height &&
		    supported_modes[i].hdr_mode == hdr_mode &&
		    supported_modes[i].exp_mode == exp_mode) {
			ox03c10->cur_mode = &supported_modes[i];
			w = ox03c10->cur_mode->hts_def - ox03c10->cur_mode->width;
			h = ox03c10->cur_mode->vts_def - ox03c10->cur_mode->height;
			__v4l2_ctrl_modify_range(ox03c10->hblank, w, w, 1, w);
			__v4l2_ctrl_modify_range(ox03c10->vblank, h,
				OX03C10_VTS_MAX - ox03c10->cur_mode->height, 1, h);
			ret = 0;
			break;
		}
	}
	return ret;
}

static int ox03c10_select_cmps_mode(struct ox03c10 *ox03c10, u32 cmps_mode)
{
	int ret = -EINVAL;
	u32 i, h, w, hdr_mode, exp_mode;
	int best_fit = -1;
	int bit_width = 0;

	w = ox03c10->cur_mode->width;
	h = ox03c10->cur_mode->height;
	hdr_mode = ox03c10->cur_mode->hdr_mode;
	exp_mode = ox03c10->cur_mode->exp_mode;
	for (i = 0; i < ARRAY_SIZE(supported_modes); i++) {
		if (w == supported_modes[i].width &&
		    h == supported_modes[i].height &&
		    supported_modes[i].hdr_mode == hdr_mode &&
		    supported_modes[i].exp_mode == exp_mode) {
			if (cmps_mode == CMPS_LOW_BIT_WIDTH_MODE) {
				if (bit_width == 0) {
					bit_width = supported_modes[i].bpp;
					best_fit = i;
				} else if (supported_modes[i].bpp < bit_width) {
					bit_width = supported_modes[i].bpp;
					best_fit = i;
				}
			} else {
				if (bit_width == 0) {
					bit_width = supported_modes[i].bpp;
					best_fit = i;
				} else if (supported_modes[i].bpp > bit_width) {
					bit_width = supported_modes[i].bpp;
					best_fit = i;
				}
			}
		}
	}
	if (best_fit >= 0) {
		ox03c10->cur_mode = &supported_modes[i];
		w = ox03c10->cur_mode->hts_def - ox03c10->cur_mode->width;
		h = ox03c10->cur_mode->vts_def - ox03c10->cur_mode->height;
		__v4l2_ctrl_modify_range(ox03c10->hblank, w, w, 1, w);
		__v4l2_ctrl_modify_range(ox03c10->vblank, h,
			OX03C10_VTS_MAX - ox03c10->cur_mode->height, 1, h);
		ret = 0;
	}
	return ret;
}

static long ox03c10_ioctl(struct v4l2_subdev *sd, unsigned int cmd, void *arg)
{
	struct ox03c10 *ox03c10 = to_ox03c10(sd);
	struct rkmodule_hdr_cfg *hdr;
	struct rkmodule_dcg_ratio *dcg_ratio;
	struct rkmodule_wb_gain_group *wb_gain_group;
	struct rkmodule_blc_group *blc_group;
	struct rkmodule_channel_info *ch_info;
	u32 i, h, w;
	long ret = 0;
	u32 stream = 0;
	u32 *exp_mode;
	struct rkmodule_wb_gain_info *wb_gain_info;
	struct rkmodule_blc_info *blc_info;

	switch (cmd) {
	case RKMODULE_GET_MODULE_INFO:
		ox03c10_get_module_inf(ox03c10, (struct rkmodule_inf *)arg);
		break;
	case RKMODULE_GET_HDR_CFG:
		hdr = (struct rkmodule_hdr_cfg *)arg;
		hdr->esp.mode = HDR_NORMAL_VC;
		hdr->hdr_mode = ox03c10->cur_mode->hdr_mode;
		if (hdr->hdr_mode == HDR_COMPR)
			hdr->compr = *ox03c10->cur_mode->hdr_compr;
		break;
	case RKMODULE_SET_HDR_CFG:
		hdr = (struct rkmodule_hdr_cfg *)arg;
		if (ox03c10->cur_mode->hdr_mode == HDR_COMPR)
			hdr->hdr_mode = ox03c10->cur_mode->hdr_mode;
		w = ox03c10->cur_mode->width;
		h = ox03c10->cur_mode->height;
		for (i = 0; i < ARRAY_SIZE(supported_modes); i++) {
			if (w == supported_modes[i].width &&
			    h == supported_modes[i].height &&
			    supported_modes[i].hdr_mode == hdr->hdr_mode) {
				ox03c10->cur_mode = &supported_modes[i];
				break;
			}
		}
		if (i == ARRAY_SIZE(supported_modes)) {
			dev_err(&ox03c10->client->dev,
				"not find hdr mode:%d %dx%d config\n",
				hdr->hdr_mode, w, h);
			ret = -EINVAL;
		} else {
			w = ox03c10->cur_mode->hts_def - ox03c10->cur_mode->width;
			h = ox03c10->cur_mode->vts_def - ox03c10->cur_mode->height;
			__v4l2_ctrl_modify_range(ox03c10->hblank, w, w, 1, w);
			__v4l2_ctrl_modify_range(ox03c10->vblank, h,
				OX03C10_VTS_MAX - ox03c10->cur_mode->height, 1, h);
		}
		break;
	case PREISP_CMD_SET_HDRAE_EXP:
		return ox03c10_set_hdrae(ox03c10, arg);
	case RKMODULE_SET_QUICK_STREAM:

		stream = *((u32 *)arg);

		if (stream)
			ret = ox03c10_write_reg(ox03c10->client, OX03C10_REG_CTRL_MODE,
				OX03C10_REG_VALUE_08BIT, OX03C10_MODE_STREAMING);
		else
			ret = ox03c10_write_reg(ox03c10->client, OX03C10_REG_CTRL_MODE,
				OX03C10_REG_VALUE_08BIT, OX03C10_MODE_SW_STANDBY);
		break;
	case RKMODULE_GET_DCG_RATIO:
		dcg_ratio = (struct rkmodule_dcg_ratio *)arg;
		*dcg_ratio = ox03c10->dcg_ratio;
		break;
	case RKMODULE_GET_SPD_RATIO:
		dcg_ratio = (struct rkmodule_dcg_ratio *)arg;
		*dcg_ratio = ox03c10->spd_ratio;
		break;
	case RKMODULE_SET_WB_GAIN:
		wb_gain_group = (struct rkmodule_wb_gain_group *)arg;
		ret = ox03c10_set_wb_gain(ox03c10, wb_gain_group);
		break;
	case RKMODULE_SET_BLC:
		blc_group = (struct rkmodule_blc_group *)arg;
		ret = ox03c10_set_blc(ox03c10, blc_group);
		break;
	case RKMODULE_GET_CHANNEL_INFO:
		ch_info = (struct rkmodule_channel_info *)arg;
		ret = ox03c10_get_channel_info(ox03c10, ch_info);
		break;
	case RKMODULE_GET_EXP_MODE:

		exp_mode = (u32 *)arg;
		*exp_mode = ox03c10->cur_mode->exp_mode;
		break;
	case RKMODULE_SET_EXP_MODE:
		ret = ox03c10_select_exp_mode(ox03c10, *(u32 *)arg);
		break;
	case RKMODULE_GET_WB_GAIN_INFO:
		wb_gain_info = (struct rkmodule_wb_gain_info *)arg;
		wb_gain_info->coarse_bit = 5;
		wb_gain_info->fine_bit = 10;
		break;
	case RKMODULE_GET_BLC_INFO:
		blc_info = (struct rkmodule_blc_info *)arg;
		blc_info->bit_width = 10;
		break;
	case RKMODULE_SET_CMPS_MODE:
		ret = ox03c10_select_cmps_mode(ox03c10, *(u32 *)arg);
		break;
	default:
		ret = -ENOIOCTLCMD;
		break;
	}

	return ret;
}

